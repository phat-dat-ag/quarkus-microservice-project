package org.example.order.dto;

public class User {
    private String id;
    private String name;
    private String email;
    private int age;

    public User() {
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }

    public int getAge() {
        return age;
    }
}
